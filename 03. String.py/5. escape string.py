# 탈출 문자

# \n : 줄바꿈
print("백문이 불여일견\n 백견이 불여일타")

# \" \' : 문장 내에서 따옴표
print('저는 "사람"입니다.') # 저는 "사람"입니다.
print("저는 \"사람\"입니다.") # 저는 "사람"입니다.

# \\ : 문장 내에서 \
print("E:\\workspace\\python-study")

# \r : 커서를 맨 앞으로 이동
print("Red Apple\rPine")

# \b : 백스페이스 (한 글자 삭제)
print("Redd\bApple")

# \t : 탭
print("Red\tApple")